T0_GRAPHENE = 2.7    # eV, nearest-neighbor hopping in graphene
HARRISON_EXP = 2.0   # Harrison's d^-2 scaling: t ∝ d^-n, n≈2

# SW defect geometry (from DFT calculations)
BOND_COMPRESSION_PENTAGON = 0.06   # Å, bonds shorten near pentagons
BOND_STRETCH_HEPTAGON = 0.08      # Å, bonds lengthen near heptagons


# ==============================================================================
# §1. GOLDEN-ANGLE QUASIPERIODIC HOPPING
# ==============================================================================

def fibonacci_word(n: int) -> List[int]:
    """
    Generate the Fibonacci word of length n.
    
    The Fibonacci word is the canonical quasiperiodic binary sequence.
    It's generated by the substitution rule A→AB, B→A, starting from A.
    The ratio of A's to B's approaches φ.
    
    In our model:
      A (=0) → bond in hexagonal region (equilibrium hopping)
      B (=1) → bond crossing a 5/7 interface (modulated hopping)
    """
    if n <= 0:
        return []
    # Build by concatenation: F(k) = F(k-1) + F(k-2)
    a, b = [0], [1]
    while len(a) < n:
        a, b = a + b, a
    return a[:n]


def golden_angle_hopping(n_sites: int, t0: float, delta_t: float, 
                          phase: float = 0.0) -> np.ndarray:
    """
    Generate hopping parameters for n_sites-1 bonds on a 1D chain,
    modulated by the golden angle.
    
    Two models provided:
    
    Model A — Aubry-André (continuous cosine modulation):
      t_i = t0 * (1 + delta_t * cos(2π i / φ² + phase))
    
    Model B — Fibonacci word (discrete modulation):
      t_i = t0 if Fibonacci[i] = A (hexagonal bond)
      t_i = t0 * (1 ± delta_t) if Fibonacci[i] = B (5/7 interface bond)
    
    We use Model A as primary (smoother, well-studied analytically).
    Model B is computed for comparison.
    
    Parameters
    ----------
    n_sites : int — number of lattice sites
    t0 : float — base hopping amplitude
    delta_t : float — modulation strength (fraction of t0)
    phase : float — phase offset for the cosine modulation
    
    Returns
    -------
    t_bonds : ndarray of shape (n_sites - 1,) — hopping per bond
    """
    n_bonds = n_sites - 1
    indices = np.arange(n_bonds)
    
    # Aubry-André with golden-angle frequency
    t_bonds = t0 * (1.0 + delta_t * np.cos(2 * np.pi * indices / PHI**2 + phase))
    
    return t_bonds


def hopping_from_geometry(t0: float = 1.0) -> Tuple[float, float, float]:
    """
    Compute hopping parameters from SW defect geometry.
    
    Using Harrison's scaling: t ∝ d^(-n), n ≈ 2
    
    Pentagon region: d = d0 - δ_pent → t_pent = t0 * (d0/(d0-δ))^n
    Heptagon region: d = d0 + δ_hept → t_hept = t0 * (d0/(d0+δ))^n
    
    Returns (t_pent, t_hex, t_hept) normalized to t0.
    """
    d0 = D0_GRAPHENE
    d_pent = d0 - BOND_COMPRESSION_PENTAGON
    d_hept = d0 + BOND_STRETCH_HEPTAGON
    
    t_pent = t0 * (d0 / d_pent) ** HARRISON_EXP
    t_hex  = t0
    t_hept = t0 * (d0 / d_hept) ** HARRISON_EXP
    
    return t_pent, t_hex, t_hept


# ==============================================================================
# §2. BOGOLIUBOV-DE GENNES HAMILTONIAN
# ==============================================================================

def build_bdg_hamiltonian(mu: float, t_bonds: np.ndarray, 
                           delta: float) -> np.ndarray:
    """
    Build the 2N × 2N BdG Hamiltonian for a Kitaev chain.
    
    H = -μ Σ c†_i c_i - Σ t_i (c†_i c_{i+1} + h.c.) + Δ Σ (c_i c_{i+1} + h.c.)
    
    In Nambu basis Ψ = (c_1, ..., c_N, c†_1, ..., c†_N):
    
    H_BdG = [[ A,  B ],
             [-B, -A ]]
    
    where A is the normal hopping matrix and B is the pairing matrix.
    
    Parameters
    ----------
    mu : float — chemical potential
    t_bonds : ndarray of shape (N-1,) — hopping amplitudes per bond
    delta : float — superconducting pairing amplitude (uniform)
    
    Returns
    -------
    H_bdg : ndarray of shape (2N, 2N)
    """
    N = len(t_bonds) + 1
    
    # Normal part: A_ii = -mu, A_{i,i+1} = A_{i+1,i} = -t_i
    A = np.zeros((N, N))
    np.fill_diagonal(A, -mu)
    for i in range(N - 1):
        A[i, i+1] = -t_bonds[i]
        A[i+1, i] = -t_bonds[i]
    
    # Pairing part: B_{i,i+1} = +Δ, B_{i+1,i} = -Δ (p-wave: antisymmetric)
    B = np.zeros((N, N))
    for i in range(N - 1):
        B[i, i+1] = delta
        B[i+1, i] = -delta
    
    # Assemble BdG matrix
    H_bdg = np.block([
        [ A,  B],
        [-B, -A]
    ])
    
    return H_bdg


def build_bdg_periodic(mu: float, t_bonds: np.ndarray, 
                        delta: float) -> np.ndarray:
    """
    BdG Hamiltonian with periodic boundary conditions.
    Used for bulk winding number computation.
    """
    N = len(t_bonds) + 1
    
    A = np.zeros((N, N))
    np.fill_diagonal(A, -mu)
    for i in range(N - 1):
        A[i, i+1] = -t_bonds[i]
        A[i+1, i] = -t_bonds[i]
    # Periodic: connect last to first with hopping = average of first and last bonds
    t_periodic = (t_bonds[0] + t_bonds[-1]) / 2
    A[0, N-1] = -t_periodic
    A[N-1, 0] = -t_periodic
    
    B = np.zeros((N, N))
    for i in range(N - 1):
        B[i, i+1] = delta
        B[i+1, i] = -delta
    B[0, N-1] = -delta  # periodic
    B[N-1, 0] = delta
    
    H_bdg = np.block([
        [ A,  B],
        [-B, -A]
    ])
    
    return H_bdg


# ==============================================================================
# §3. DIAGONALIZATION AND ZERO MODE DETECTION
# ==============================================================================

def find_zero_modes(H_bdg: np.ndarray, 
                     threshold: float = 1e-6) -> Tuple[np.ndarray, np.ndarray, int]:
    """
    Diagonalize the BdG Hamiltonian and find zero-energy modes.
    
    Returns
    -------
    energies : all eigenvalues sorted by |E|
    states : corresponding eigenvectors
    n_zero : number of eigenvalues with |E| < threshold
    """
    eigenvalues, eigenvectors = np.linalg.eigh(H_bdg)
    
    # Sort by absolute energy
    sort_idx = np.argsort(np.abs(eigenvalues))
    energies = eigenvalues[sort_idx]
    states = eigenvectors[:, sort_idx]
    
    n_zero = np.sum(np.abs(energies) < threshold)
    
    return energies, states, n_zero


def check_edge_localization(states: np.ndarray, n_zero: int, 
                             N: int, edge_sites: int = 5) -> dict:
    """
    Check whether zero modes are localized at the chain edges.
    
    For a Kitaev chain with N sites, the BdG eigenvector has 2N components:
    first N are particle, last N are hole.
    
    A Majorana zero mode should have weight concentrated at the first 
    or last few sites.
    """
    if n_zero == 0:
        return {"localized": False, "left_weight": 0.0, "right_weight": 0.0}
    
    results = []
    for i in range(min(n_zero, 4)):  # Check up to 4 zero modes
        psi = states[:, i]
        # Total probability on particle + hole components at each site
        prob = np.abs(psi[:N])**2 + np.abs(psi[N:])**2
        prob /= prob.sum()
        
        left_weight = prob[:edge_sites].sum()
        right_weight = prob[-edge_sites:].sum()
        
        results.append({
            "mode": i,
            "energy": float(np.abs(states[:, i] @ np.zeros(2*N))),  # placeholder
            "left_weight": float(left_weight),
            "right_weight": float(right_weight),
            "edge_total": float(left_weight + right_weight),
        })
    
    # MZMs are edge-localized if combined edge weight > 50%
    best = max(results, key=lambda r: r["edge_total"])
    
    return {
        "localized": best["edge_total"] > 0.5,
        "left_weight": best["left_weight"],
        "right_weight": best["right_weight"],
        "modes": results,
    }


