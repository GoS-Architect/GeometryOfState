# §5. PHASE BOUNDARY SWEEP
# ==============================================================================

def sweep_mu(t0: float, delta_t: float, delta: float, n_sites: int,
             mu_range: np.ndarray, threshold: float = 1e-4) -> dict:
    """
    Sweep chemical potential μ and detect the topological phase boundary.
    
    Returns dict with:
      mu_values, gap_values, n_zero_modes, is_topological
    """
    t_bonds = golden_angle_hopping(n_sites, t0, delta_t)
    
    results = {
        "mu": [],
        "gap": [],
        "n_zero": [],
        "topological": [],
        "min_energy": [],
    }
    
    for mu in mu_range:
        H = build_bdg_hamiltonian(mu, t_bonds, delta)
        energies, states, n_zero = find_zero_modes(H, threshold)
        
        # Gap = smallest positive energy (excluding zero modes)
        positive_E = np.sort(np.abs(energies))
        gap = positive_E[n_zero] if n_zero < len(positive_E) else 0.0
        
        results["mu"].append(float(mu))
        results["gap"].append(float(gap))
        results["n_zero"].append(int(n_zero))
        results["topological"].append(n_zero >= 2)  # need at least 2 (left + right MZM)
        results["min_energy"].append(float(positive_E[0]))
    
    return results


def sweep_modulation(t0: float, mu: float, delta: float, n_sites: int,
                      delta_t_range: np.ndarray, 
                      threshold: float = 1e-4) -> dict:
    """
    Sweep modulation strength to find critical amplitude.
    """
    results = {
        "delta_t": [],
        "gap": [],
        "n_zero": [],
        "topological": [],
    }
    
    for dt in delta_t_range:
        t_bonds = golden_angle_hopping(n_sites, t0, dt)
        H = build_bdg_hamiltonian(mu, t_bonds, delta)
        energies, states, n_zero = find_zero_modes(H, threshold)
        
        positive_E = np.sort(np.abs(energies))
        gap = positive_E[n_zero] if n_zero < len(positive_E) else 0.0
        
        results["delta_t"].append(float(dt))
        results["gap"].append(float(gap))
        results["n_zero"].append(int(n_zero))
        results["topological"].append(n_zero >= 2)
    
    return results


