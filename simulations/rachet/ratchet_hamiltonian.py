# §4. WINDING NUMBER (BULK INVARIANT)
# ==============================================================================

def winding_number_uniform(mu: float, t: float, delta: float, 
                            n_k: int = 1000) -> float:
    """
    Compute the winding number for a uniform Kitaev chain.
    
    H(k) = d_z(k) σ_z + d_y(k) σ_y
    where d_z = -2t cos(k) - μ,  d_y = 2Δ sin(k)
    
    w = (1/2π) ∮ [d_z ∂_k(d_y) - d_y ∂_k(d_z)] / (d_z² + d_y²) dk
    """
    k = np.linspace(0, 2 * np.pi, n_k, endpoint=False)
    dk = 2 * np.pi / n_k
    
    d_z = -2 * t * np.cos(k) - mu
    d_y = 2 * delta * np.sin(k)
    
    # Derivatives
    dd_z = 2 * t * np.sin(k)
    dd_y = 2 * delta * np.cos(k)
    
    # Winding integrand
    denom = d_z**2 + d_y**2
    # Avoid division by zero at gap closing
    denom = np.maximum(denom, 1e-30)
    
    integrand = (d_z * dd_y - d_y * dd_z) / denom
    w = np.sum(integrand) * dk / (2 * np.pi)
    
    return w


def winding_number_quasiperiodic(mu: float, t_bonds: np.ndarray, 
                                   delta: float) -> float:
    """
    Compute the winding number for a quasiperiodic chain via the
    real-space method (Bott index / spectral localizer approach).
    
    For a periodic approximant: compute the winding from the 
    Q-matrix (flattened Hamiltonian).
    
    w = Tr(Q[Q, X]) / (2πi) where Q = sign(H_BdG), X = position
    
    For practical purposes, we use the eigenvalue signature:
    the number of negative eigenvalues of H_BdG gives the 
    ground state parity, and the presence of zero modes
    signals the topological phase.
    """
    N = len(t_bonds) + 1
    
    # Build periodic BdG
    H = build_bdg_periodic(mu, t_bonds, delta)
    eigenvalues = np.linalg.eigvalsh(H)
    
    # Spectral asymmetry: count eigenvalues below zero
    n_neg = np.sum(eigenvalues < -1e-10)
    n_pos = np.sum(eigenvalues > 1e-10)
    n_zero_approx = 2 * N - n_neg - n_pos
    
    # For the Bott index: use the position operator and Q = sign(H)
    Q = np.zeros_like(H)
    evals, evecs = np.linalg.eigh(H)
    signs = np.sign(evals)
    signs[np.abs(evals) < 1e-10] = 0  # zero modes
    Q = evecs @ np.diag(signs) @ evecs.T
    
    # Position operator (diagonal, sites 0 to N-1, duplicated for particle/hole)
    X = np.diag(np.concatenate([np.arange(N) / N, np.arange(N) / N]))
    
    # Bott index
    commutator = Q @ X - X @ Q
    bott = np.trace(Q @ commutator).imag / (2 * np.pi)
    
    return bott


